// ======================================
// 计算分数
// ======================================


questions.forEach((q,index)=>{


    const answer = answers[index];


    if(answer === undefined){
        return;
    }



    const option = q.options[answer];



    if(!option.score){
        return;
    }



    for(let key in option.score){


        scores[key] =
        (scores[key] || 0)
        +
        option.score[key];


    }


});



console.log("能力模型:",scores);



// ======================================
// 获取最高天赋
// ======================================


let talentType = "";

let maxScore = -1;



for(let key in scores){


    if(scores[key] > maxScore){

        maxScore = scores[key];

        talentType = key;

    }

}



