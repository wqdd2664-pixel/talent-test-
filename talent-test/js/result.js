// 生成报告图片

window.addEventListener("load",()=>{


    const reportBtn = document.getElementById("generateReport");


    if(!reportBtn){

        console.log("没有找到生成报告按钮");

        return;

    }



    reportBtn.onclick = ()=>{


        const report =
        document.getElementById("report");



        console.log("截图对象:", report);



        if(!report){

            alert("没有找到报告内容区域");

            return;

        }



        html2canvas(report,{

            scale:2,

            backgroundColor:"#f7f2e8",

            useCORS:true


        }).then(canvas=>{


            const link =
            document.createElement("a");



            link.download =
            "我的天赋报告.png";



            link.href =
            canvas.toDataURL("image/png");



            link.click();



        }).catch(err=>{


            console.error("生成图片失败:",err);


        });



    };


});